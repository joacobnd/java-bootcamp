package com.joaco;



public class Main {

    public static void main(String[] args) {
        // Else If

        int age = 0;
        boolean isAdult = age >= 18;

        if (isAdult) {
            System.out.println("is adult :)");
        } else if (age == 0) {
            System.out.println("baby :)");
        } else if (age == 1) {
            System.out.println("toddler :)");
        } else if (age == 2) {
            System.out.println("toddler :)");
        } else if (age == 3) {
            System.out.println("toddler :)");
        } else {
            System.out.println("is not adult :(");
        }


    }

}


package com.joaco;



public class Main {

    public static void main(String[] args) {
        // Loops


        for (int i = 0; i < 10 ; i++) {
            System.out.println(i + " Hello!");
        }

        System.out.println("0");
        //Reverse loop
        for (int i = 10; i >= 0 ; i--) {
            System.out.println(i + " Hello!");
        }

    }

}


package com.joaco;

public class Main {

    public static void main(String[] args) {

        //Comparison Operators
        // < <= > >= == !=

        System.out.println(10 < 10);
        System.out.println(10 <= 10);
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);

        boolean isAdult = 18 >= 16;

        System.out.println();
        System.out.println(isAdult);


    }
}